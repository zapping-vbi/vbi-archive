void
init_decoder(struct decode_rec *d,
	int raw_bytes, int sampling_rate, int bit_rate,
	unsigned int frc, unsigned int frc_mask, int payload)
{
	unsigned int
	int_reverse(unsigned int n)
	{
		return (bit_reverse[n & 0xFF] << 24)
		       + (bit_reverse[(n >> 8) & 0xFF] << 16)
		       + (bit_reverse[(n >> 16) & 0xFF] << 8)
		       + bit_reverse[(n >> 24) & 0xFF];
	};

	d->frc_mask		= int_reverse(frc_mask);
	d->framing_code 	= int_reverse(frc) & d->frc_mask;
	d->frc_bytes		= raw_bytes - sampling_rate * (8 * payload + 2) / bit_rate;
	d->step8		= sampling_rate * 256.0 / bit_rate;
	d->bit_rate		= bit_rate;
	d->oversampling_rate	= sampling_rate * 4;
	d->payload		= payload;
	d->thresh		= 105 << 10;
}

bool
decode_nrz(struct decode_rec *d, unsigned char *raw, unsigned char *buf)
{
	int i, j, k;
        int tr, b, b1 = 0, cl = 0;
	unsigned int c = 0, t, s;

	for (i = d->frc_bytes; i > 0; raw++, i--) {
		tr = d->thresh >> 10;
		d->thresh += ((int) raw[0] - tr) * abs(raw[1] - raw[0]);
		t = raw[0] * 4;

		for (j = 4; j > 0; j--) {
			s = (t + 2) >> 2;
			b = (s >= tr);

    			if (b ^ b1) {
				cl = d->oversampling_rate >> 1;
			} else {
				cl += d->bit_rate;

				if (cl >= d->oversampling_rate) {
					cl -= d->oversampling_rate;

					c = c * 2 + b;

					if ((c & d->frc_mask) == d->framing_code) {
						i = d->step8 + 128;
						c = 0;

						for (j = d->payload; j > 0; j--) {
							for (k = 8; k > 0; k--) {
						    		c >>= 1;
								c |= (raw[i >> 8] >= tr) << 7;
			    					i += d->step8;
							}

							*buf++ = c;
				    		}

			    			return TRUE;
					}
				}
			}

			b1 = b;
			t += raw[1];
			t -= raw[0];
		}
	}

	return FALSE;
}
